def _build_gtk_css(design: dict) -> str:
    """Build GTK 3/4 CSS color overrides from the active palette."""
    p = design.get("palette", {})
    name = design.get("name", "theme")
    background = p.get("background", "#1e1e2e")
    foreground = p.get("foreground", "#cdd6f4")
    primary = p.get("primary", "#89b4fa")
    surface = p.get("surface", "#313244")
    muted = p.get("muted", "#6c7086")
    danger = p.get("danger", "#f38ba8")
    success = p.get("success", "#a6e3a1")
    warning = p.get("warning", "#f9e2af")

    return f"""/* Generated by linux-ricing — {name} */
@define-color accent_color {primary};
@define-color accent_bg_color {primary};
@define-color accent_fg_color {background};
@define-color destructive_color {danger};
@define-color success_color {success};
@define-color warning_color {warning};
@define-color error_color {danger};

window {{
  background-color: {background};
  color: {foreground};
}}

headerbar {{
  background-color: {surface};
  color: {foreground};
  border-bottom: 1px solid {primary};
}}

entry {{
  background-color: {surface};
  color: {foreground};
  border: 1px solid {muted};
}}

entry:focus {{
  border-color: {primary};
}}

button {{
  background-color: {surface};
  color: {foreground};
}}

button:hover {{
  background-color: {primary};
  color: {background};
}}
"""


def materialize_gtk(design: dict, backup_ts: str, dry_run: bool = False) -> list[dict]:
    """Apply GTK theme, icon theme, cursor theme, and CSS overrides."""
    palette = design["palette"]
    template_path = TEMPLATES_DIR / "gtk" / "settings.ini.template"
    changes = []

    is_dark = is_dark_palette(palette)
    gtk_theme    = design.get("gtk_theme",    "Adwaita-dark" if is_dark else "Adwaita")
    icon_theme   = design.get("icon_theme",   "Papirus-Dark" if is_dark else "Papirus")
    cursor_theme = design.get("cursor_theme", "default")

    ui_font = design.get("typography", {}).get("ui_font", "JetBrains Mono")
    context = {
        **palette,
        "name": design.get("name", "theme"),
        "gtk_theme": gtk_theme,
        "icon_theme": icon_theme,
        "cursor_theme": cursor_theme,
        "font_name": f"{ui_font} 10",
        "prefer_dark": "1" if is_dark else "0",
    }
    settings_content = render_template(template_path, context)
    css_content = _build_gtk_css(design)

    if dry_run:
        changes.append({"app": "gtk", "action": "dry-run", "gtk_theme": gtk_theme,
                        "icon_theme": icon_theme, "cursor_theme": cursor_theme, "dark": is_dark,
                        "gtk3_css": str(HOME / ".config" / "gtk-3.0" / "gtk.css"),
                        "gtk4_css": str(HOME / ".config" / "gtk-4.0" / "gtk.css")})
        return changes

    for gtk_dir_name in ["gtk-3.0", "gtk-4.0"]:
        gtk_dir = HOME / ".config" / gtk_dir_name
        gtk_dir.mkdir(parents=True, exist_ok=True)
        settings_path = gtk_dir / "settings.ini"
        backup = backup_file(settings_path, backup_ts, f"gtk/{gtk_dir_name}/settings.ini")
        settings_path.write_text(settings_content, encoding="utf-8")
        changes.append({"app": "gtk", "action": "write", "path": str(settings_path), "backup": backup})

        css_path = gtk_dir / "gtk.css"
        css_backup = backup_file(css_path, backup_ts, f"gtk/{gtk_dir_name}/gtk.css")
        css_path.write_text(css_content, encoding="utf-8")
        changes.append({"app": "gtk", "action": "write", "path": str(css_path), "backup": css_backup})

    if cmd_exists("gsettings"):
        schema = "org.gnome.desktop.interface"
        for key, val in [("gtk-theme", gtk_theme), ("icon-theme", icon_theme), ("cursor-theme", cursor_theme)]:
            rc, _, _ = run_cmd(["gsettings", "set", schema, key, val])
            changes.append({"app": "gtk", "action": "gsettings", "key": key, "value": val, "success": rc == 0})

    # Expose GTK config dirs so Flatpak apps read our settings.ini (--user, no sudo).
    if cmd_exists("flatpak") and design.get("apply_flatpak_overrides", True):
        flatpak_overrides = [
            ["--filesystem", "xdg-config/gtk-3.0:ro"],
            ["--filesystem", "xdg-config/gtk-4.0:ro"],
            ["--filesystem", "xdg-data/icons:ro"],
        ]
        ok = True
        for args in flatpak_overrides:
            rc, _, _ = run_cmd(["flatpak", "override", "--user"] + args, timeout=10)
            if rc != 0:
                ok = False
        changes.append({"app": "gtk", "action": "flatpak-override",
                        "gtk_theme": gtk_theme, "icon_theme": icon_theme, "success": ok})

    return changes


