def materialize_fastfetch(design: dict, backup_ts: str, dry_run: bool = False) -> list[dict]:
    """Rewrite ~/.config/fastfetch/config.jsonc with palette-derived colors.

    Writes to ``config.jsonc`` (fastfetch's preferred and only auto-loaded
    filename); a legacy ``config.json`` left from older runs is moved to
    backup so it does not confuse future maintenance.
    """
    palette = design["palette"]
    mood_tags = design.get("mood_tags", [])
    name = design.get("name", "theme")
    fastfetch_dir = HOME / ".config" / "fastfetch"
    config_path = fastfetch_dir / "config.jsonc"
    legacy_json_path = fastfetch_dir / "config.json"
    changes = []

    if any(t in mood_tags for t in ["gothic", "blood", "fantasy", "dark-fantasy"]):
        separator = " ♦ "
    elif any(t in mood_tags for t in ["game", "rpg", "maplestory", "pixel"]):
        separator = " ♥ "
    elif any(t in mood_tags for t in ["void", "dragon", "cyber", "neon"]):
        separator = " 𑁍 "
    else:
        separator = " ─ "

    # Fastfetch 2.x requires the leading '#' on hex color values; without it
    # the binary aborts with "invalid color code found: <bare-hex>".
    config = {
        "logo":    {"type": "auto", "color": {"1": palette["primary"], "2": palette["accent"]}},
        "display": {"separator": separator, "color": {
            "title": palette["primary"],
            "keys":  palette["accent"],
            "separator": palette["warning"],
        }},
        "modules": [
            {"type": "title",    "key": f"  {name.upper()}",  "keyColor": palette["primary"]},
            {"type": "separator","string": "─"},
            {"type": "os",       "key": "  OS",     "keyColor": palette["primary"]},
            {"type": "kernel",   "key": "  Kernel", "keyColor": palette["accent"]},
            {"type": "uptime",   "key": "  Uptime", "keyColor": palette["success"]},
            {"type": "de",       "key": "  WM",     "keyColor": palette["secondary"]},
            {"type": "terminal", "key": "  Term",   "keyColor": palette["primary"]},
            {"type": "shell",    "key": "  Shell",  "keyColor": palette["accent"]},
            {"type": "cpu",      "key": "  CPU",    "keyColor": palette["warning"]},
            {"type": "memory",   "key": "  RAM",    "keyColor": palette["danger"]},
            {"type": "colors",   "paddingLeft": 2,  "symbol": "circle"},
        ],
    }

    if dry_run:
        changes.append({"app": "fastfetch", "action": "dry-run", "path": str(config_path)})
        return changes

    fastfetch_dir.mkdir(parents=True, exist_ok=True)
    config_backup = backup_file(config_path, backup_ts, "fastfetch/config.jsonc")
    config_path.write_text(json.dumps(config, indent=2, ensure_ascii=False), encoding="utf-8")
    changes.append({"app": "fastfetch", "action": "write", "path": str(config_path), "backup": config_backup})

    # Retire any legacy config.json so future inspectors don't confuse it
    # with the active config.jsonc that fastfetch actually loads.
    if legacy_json_path.exists():
        legacy_backup = backup_file(legacy_json_path, backup_ts, "fastfetch/config.json")
        legacy_json_path.unlink()
        changes.append({"app": "fastfetch", "action": "retire-legacy",
                        "path": str(legacy_json_path), "backup": legacy_backup})
    return changes


